const renderHeatMap = ({ data, levels, startDay, fill }) => {
	// let resultString = "–ø–Ω –≤—Ç —Å—Ä —á—Ç –ø—Ç —Å–± –≤—Å\n"; // —Ä–µ–∑—É–ª—å—Ç–∏—Ä—É—é—â–∞—è —Å—Ç—Ä–æ–∫–∞ —Å heatMap
  
	let resultString = "";

	let dayOfTheWeek = startDay; // –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏, –Ω–µ–æ–±—Ö–æ–¥–∏–º –¥–ª—è –ø–µ—Ä–µ—Ö–æ–¥–∞ –Ω–∞ –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É –≤ –∫–æ–Ω—Ü–µ –Ω–µ–¥–µ–ª–∏
	for (let i = 0; i < startDay; i++) // –¥–æ–±–∞–≤–ª–µ–Ω–∏–µ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ–ª—è –¥–æ –Ω—É–∂–Ω–æ–≥–æ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
		resultString += fill;

	// –ø—Ä–æ–±–µ–≥–∞–µ–º—Å—è –ø–æ –∫–∞–∂–¥–æ–º—É —á–∏—Å–ª—É –≤ data
	data.forEach(d => {
		let emoji = fill; // –º–∞–ª–æ –ª–∏ –≤–æ–∑–Ω–∏–∫–Ω–µ—Ç –æ—à–∏–±–∫–∞ —Å —É—Å–ª–æ–≤–∏–µ–º, —Ç–æ–≥–¥–∞ –¥–æ–±–∞–≤–æ—á–Ω—ã–π —ç–º–æ–¥–∂–∏ –±—É–¥–µ—Ç –∫–∞–∫ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ–ª—å –æ–ø—Ä–µ–¥–µ–ª—è—Ç—å—Å—è
		for (let i = 0; i < levels.length; i++) {
			// –Ω–∞—Ö–æ–¥–∏–º –Ω—É–∂–Ω—ã–π —ç–º–æ–¥–∂–∏ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Ä–æ–≤–Ω–µ–º
			if (d >= levels[i].min && (d <= levels[i].max || levels[i].max == -1)) {
				emoji = levels[i].value;
				break;
			}
		}
		resultString += emoji; // –¥–æ–±–∞–≤–ª—è–µ–º —ç–º–æ–¥–∂–∏ –≤ heatMap

		// –Ω–∞—Ä–∞—â–∏–≤–∞–µ–º –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ –∏ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ –ø–µ—Ä–µ—Ö–æ–¥–∏–º –Ω–∞ –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É
		dayOfTheWeek++;
		if (dayOfTheWeek == 7) {
			resultString += "\n";
			dayOfTheWeek = 0;
		}
	});

	return resultString;
}